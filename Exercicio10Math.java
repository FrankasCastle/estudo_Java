public class Exercicio10Math {
    public static void main (String [] args){
          /* Math.max(x,y)
        The Math.max(x,y) method can be used to find the
        highest value of x and y. */

        System.out.println(Math.max(5, 10));

        /* Math.min(x,y)
        Same thing, but you find the lowest value of x and y. */

        System.out.println(Math.max(5,10));

        /* Math.sqrt(x)
        The Math.sqrt(x) method retruns the square root of x.*/

        System.out.println(Math.sqrt(64));

        /* Math.abs(x)
        The Math.abs(x) method returns the absolute (positive)
        value of x. */

        System.out.println(Math.abs(-4.7));

        /* Math.pow(x,y)
        The Math.pow(x,y) method retuns the value of x raised
        to power of y.*/

        System.out.println(Math.pow(2, 8));

        /* Rounding Methods
        Math.round(x) rounds to the nearest integer.
        Math.ceil(x) rounds up (returns the smallest integer
        greater than or equal to x)
        Math.floor(x) rounds down (returns the largest integer
        less than or equal to x) */

        System.out.println(Math.round(4.6));
        System.out.println(Math.ceil(4.1));
        System.out.println(Math.floor(4.9));

        /* Random Numbers
        Math.random() returns a random number between 0.0(inclusive),
        and 1.0(exclusive). */

        System.out.println(Math.random());




    }
}